<script setup>
import { onMounted, nextTick } from 'vue';

function animateCounters(){
  const counters = document.querySelectorAll('.counter');
  
  counters.forEach(counter => {
    const target = +counter.dataset.target;
    const duration = 2000; // 2 seconds
    const startTime = Date.now();
    
    function updateCounter() {
      const elapsed = Date.now() - startTime;
      const progress = Math.min(elapsed / duration, 1); // 0 to 1
      
      // Easing function (easeOutExpo equivalent)
      const easeProgress = 1 - Math.pow(2, -10 * progress);
      
      const currentValue = Math.floor(target * easeProgress);
      counter.innerHTML = currentValue;
      
      if (progress < 1) {
        requestAnimationFrame(updateCounter);
      } else {
        counter.innerHTML = target;
      }
    }
    
    updateCounter();
  });
}

function setupScrollAnimations() {
  const observerOptions = {
    threshold: 0.1,
    rootMargin: '0px 0px -50px 0px'
  };

  const observer = new IntersectionObserver((entries) => {
    entries.forEach(entry => {
      if (entry.isIntersecting && !entry.target.classList.contains('visible')) {
        entry.target.classList.add('visible');
        
        // Animate counters when section becomes visible
        const countersInSection = entry.target.querySelectorAll('.counter');
        countersInSection.forEach(counter => {
          if (counter.innerHTML === '0') {
            animateCountersForSection(entry.target);
          }
        });
      }
    });
  }, observerOptions);

  // Observe all content sections
  document.querySelectorAll('.content-section').forEach(section => {
    observer.observe(section);
  });
  
  // Also observe intro section
  const introSection = document.querySelector('.intro-section');
  if (introSection) {
    observer.observe(introSection);
  }
}

function animateCountersForSection(section) {
  const counters = section.querySelectorAll('.counter');
  
  counters.forEach(counter => {
    const target = +counter.dataset.target;
    const duration = 2000; // 2 seconds
    const startTime = Date.now();
    
    function updateCounter() {
      const elapsed = Date.now() - startTime;
      const progress = Math.min(elapsed / duration, 1); // 0 to 1
      
      // Easing function (easeOutExpo equivalent)
      const easeProgress = 1 - Math.pow(2, -10 * progress);
      
      const currentValue = Math.floor(target * easeProgress);
      counter.innerHTML = currentValue;
      
      if (progress < 1) {
        requestAnimationFrame(updateCounter);
      } else {
        counter.innerHTML = target;
      }
    }
    
    updateCounter();
  });
}

onMounted(async () => {
  await nextTick();
  
  // Setup scroll-based animations
  setupScrollAnimations();
});
</script>

